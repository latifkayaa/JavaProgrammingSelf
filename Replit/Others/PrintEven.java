package Others;

public class PrintEven {
    public static void main(String[] args) {

        for (int i = 80; i >=20 ; i=i-2) {
            if(i<=80 && i>20){
                System.out.print(i+ " ");
            }
            else if(i==20){
                System.out.print(i);

            }
        }
    }
}
