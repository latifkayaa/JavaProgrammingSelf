package day09_IfElseStatements;

public class MinimumNumberIfElse {
    public static void main(String[] args) {

        int a = 123, b=58;

        if(a<b){
            System.out.println("The minimum number is: " + a);
        }
        else {
            System.out.println("The minimum number is: " + b);
        }



    }
}
